package drz.oddb.parseStmt;

public class DeputyAttr {
	public String deputyname;
	public String switchrule;
	public boolean isDeputy;
	@Override
	public String toString() {
		return "DeputyAttr{" +
				"deputyname='" + deputyname + '\'' +
				", switchrule='" + switchrule + '\'' +
				", isDeputy=" + isDeputy +
				'}';
	}
}
